package com.bridgelabz.programs.ObjectOrientedProgram.stock;

public class UserList 
{
		private String name ;
		private int  buyShare ;
		private int  sellShare ;
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getBuyShare() {
			return buyShare;
		}
		public void setBuyShare(int buyShare) {
			this.buyShare = buyShare;
		}
		public int getSellShare() {
			return sellShare;
		}
		public void setSellShare(int sellShare) {
			this.sellShare = sellShare;
		}
		
		
}
